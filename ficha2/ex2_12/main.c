#include <stdio.h>

int main() {
	printf("(2 > 7 ? 3 : 4)         => %d\n", (2 > 7 ? 3 : 4));
	printf("(5 > 20) || 20 > 5)     => %d\n", (5 > 20) || 20 > 5);
	printf("(!(73 % 2 != 0))        => %d\n", (!(73 % 2 != 0)));
	printf("(21.6 / 3.0 + 3.0)      => %f\n", (21.6 / 3.0 + 3.0));
	printf("(5 % 3 * 2 + 7 >= 2)    => %d\n", (5 % 3 * 2 + 7 >= 2));
}